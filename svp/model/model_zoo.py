import torch
from timm.models.registry import register_model
from timm.models.vision_transformer import _cfg
from .models_mamba import VisionMamba

@register_model
def vim_tiny(pretrained=False, **kwargs):
    model = VisionMamba(
        patch_size=16, embed_dim=192, depth=24, rms_norm=True, residual_in_fp32=True, fused_add_norm=True, if_abs_pos_embed=True, if_rope=False, if_rope_residual=False, bimamba_type="v2", if_devide_out=True, **kwargs)
    model.default_cfg = _cfg()
    if pretrained:
        checkpoint = torch.hub.load_state_dict_from_url(
            url="to.do",
            map_location="cpu", check_hash=True
        )
        model.load_state_dict(checkpoint["model"])
    return model

